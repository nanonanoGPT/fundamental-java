package metode_java;

public class Metode_overloading {

    /*
        => dengan overloading , beberapa metode dapat memiliki nama yg sama dengan parameter berbeda
        => contoh

        int myMethod(int x);
        float myMethod(float x)
        double myMethod(double x)
     */


     public static int penjumlahan(int x, int y){
        return x + y;
     }

     public static float penjumlahan(float x, float y){
        return x + y;
     }

    
}
