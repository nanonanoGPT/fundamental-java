package basic;

public class If_else {



    public static void if_else(){


        int time = 22;


        //  if else normal
        if(time < 10) {
            System.out.println("salah");
        } else {
            System.out.println("bener");
        }


        //  if ternary
        
        //  deklarasi 
        // variable = (condition) ? true : false;


        String result = (time < 18) ? "Good day" : "Good evening";

    }
    
}
