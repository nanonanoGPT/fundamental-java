package java_oop;

public class konstruktor {


    /* 
            => methode kusus yg di gunakan untuk menginisialiasi object 
            => konstruktor di panggil ketika object suatu kelas di buat 
            => ini dapat di gunaka untuk menetapkan nilai awal
            => penulisan nama konstruktor harus sama dengan nama kelas 

     */


     public int modelYear;
     public String modelName;;

     public konstruktor(int year, String name){
        modelYear = year;
        modelName = name;



     }
    
}
